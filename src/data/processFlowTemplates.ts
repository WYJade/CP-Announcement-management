export function getCurrentStep(template: ScenarioFlowTemplate, status: WorkItemStatus): string {
  return template.statusToStep[status] ?? template.steps[0].id
}

export function getStepCompletionStatus(
  template: ScenarioFlowTemplate,
  status: WorkItemStatus
): Record<string, 'completed' | 'active' | 'pending'> {
  const currentStepId = getCurrentStep(template, status)
  const currentIndex = template.steps.findIndex((s) => s.id === currentStepId)
  const isTerminal = ['Closed', 'Cancelled', 'Resolved'].includes(status)

  const result: Record<string, 'completed' | 'active' | 'pending'> = {}
  template.steps.forEach((step, i) => {
    if (isTerminal && status === 'Closed') {
      result[step.id] = 'completed'
    } else if (i < currentIndex) {
      result[step.id] = 'completed'
    } else if (i === currentIndex) {
      result[step.id] = 'active'
    } else {
      result[step.id] = 'pending'
    }
  })
  return result
}
